function myFunction() {
  var message, x;
  message = document.getElementById("type");
  message.innerHTML = "";
  x = document.getElementById("rate").value;
  try { 
    if(x == "")  throw "empty";
    if(isNaN(x)) throw "not a number";
    x = Number(x);
    if(x < 1)  throw "too low";
    if(x > 10)   throw "too high";
  }
  catch(err) {
    message.innerHTML = "Input is " + err;
  }
}